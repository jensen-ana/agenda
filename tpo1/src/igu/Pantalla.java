    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //al presionar guardar, Grabar datos en cada vector
        nombres[indice] = txtNombre.getText();
        apellidos[indice] = txtApellido.getText();
        dnis[indice] = txtDni.getText();
        fnacs[indice] = txtFNac.getText();
        direcciones[indice] = txtDireccion.getText();
        telefonos[indice] = txtTelefono.getText();
        //imprimo un mensaje avisando de que fue guardado correctamente
        txtEstatus.setText("El contacto fue guardado exitosamente");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed

        //si es menor a nueve muevo la pagina y muestro el contenido del próximo vector    
        if (indice < 9) {
            //agrego al indice uno, para mover la pagina       
            indice = indice + 1;
            //escribo en todos los espacios correspondientes, el dato del indice en cuestion      
            txtNombre.setText(nombres[indice]);
            txtApellido.setText(apellidos[indice]);
            txtDni.setText(dnis[indice]);
            txtFNac.setText(fnacs[indice]);
            txtDireccion.setText(direcciones[indice]);
            txtTelefono.setText(telefonos[indice]);
            //imprimo el numero correspondiente al vector que estamos usando
            txtIndice.setText(String.valueOf(indice));

            //pongo el mensaje de status en blanco
            txtEstatus.setText("");
            //habilito el boton de nuevo
            btnPrev.setEnabled(true);
        } 
        // si no, muestro por pantalla un mensaje de error y deshabilito el boton
        else {
            txtEstatus.setText("La accíon es imposible de ejecutar");
            btnNext.setEnabled(false);
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed

        //si es mayor a 0, muevo la pagina         
        if (indice > 0) {
            //le quito una unidad al indice, por el boton "previo"
            indice = indice - 1;
            txtNombre.setText(nombres[indice]);
            txtApellido.setText(apellidos[indice]);
            txtDni.setText(dnis[indice]);
            txtFNac.setText(fnacs[indice]);
            txtDireccion.setText(direcciones[indice]);
            txtTelefono.setText(telefonos[indice]);
            //imprimo el numero correspondiente al vector que estamos usando
            txtIndice.setText(String.valueOf(indice));
            //pongo el mensaje de status en blanco
            txtEstatus.setText("");
            //hablilto el boton siguiente
            btnNext.setEnabled(true);
        } 
        // si no muestro mensaje de error y deshabilito el boton previo
        else {
            txtEstatus.setText("La accíon es imposible de ejecutar");
            //deshablito el boton 
            btnPrev.setEnabled(false);
        }
    }//GEN-LAST:event_btnPrevActionPerformed


